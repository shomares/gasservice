package com.ishimura.gasService.dto;

public class Vehiculo {

	private String marca;
	private String nombre;
	private String ano;
	private String transmision;
	private String combustible;
	private Double rendimientoPorLitro;
	private Double emision;
	private Double emisionNOX;
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getAno() {
		return ano;
	}
	public void setAno(String ano) {
		this.ano = ano;
	}
	public String getTransmision() {
		return transmision;
	}
	public void setTransmision(String transmision) {
		this.transmision = transmision;
	}
	public String getCombustible() {
		return combustible;
	}
	public void setCombustible(String combustible) {
		this.combustible = combustible;
	}
	public Double getRendimientoPorLitro() {
		return rendimientoPorLitro;
	}
	public void setRendimientoPorLitro(Double rendimientoPorLitro) {
		this.rendimientoPorLitro = rendimientoPorLitro;
	}
	public Double getEmision() {
		return emision;
	}
	public void setEmision(Double emision) {
		this.emision = emision;
	}
	public Double getEmisionNOX() {
		return emisionNOX;
	}
	public void setEmisionNOX(Double emisionNOX) {
		this.emisionNOX = emisionNOX;
	}
	
	
	
	
	
	
	
	
}
